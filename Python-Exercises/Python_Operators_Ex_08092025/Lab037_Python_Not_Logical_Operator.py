# Logical Operator --> bool
# >, <, >=, <=

x = 10
y = 20
print( x > y ) # False
print( x < y ) # True

print(" -------- ")

a = 10
b = 10
print(a == b) # True
print(a >= b) # True 10 > 10 or 10 = 10
print(a <= b) # True 10 < 10 or 10 = 10
