# |0 | 0==5 - False | o/p = 0
# |1 | 1==5 - False | o/p = 1
# |2 | 2==5 - False | o/p = 2
# |3 | 3==5 - False | o/p = 3
# |4 | 4==5 - False | o/p = 4
# |5 | 5==5 - True | o/p = Five
# |6 | 6==5 - False | o/p = 6
# |10 | 10==5 - False | For loop finished - o/p = Exit


