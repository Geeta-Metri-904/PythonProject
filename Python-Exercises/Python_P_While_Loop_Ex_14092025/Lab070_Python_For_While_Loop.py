for i in range(0, 10):  # 0 to 9 - 10 times
    print(i)
    if i == 5:
        break

# |i | Condition | o/p
# |0 | 0==5 - False | o/p = 0
# |1 | 1==5 - False | o/p = 1
# |2 | 2==5 - False | o/p = 2
# |3 | 3==5 - False | o/p = 3
# |4 | 4==5 - False | o/p = 4
# |5 | 5==5 - True | o/p = Break out of For loop

# output :
# 0
# 1
# 2
# 3
# 4
# 5
